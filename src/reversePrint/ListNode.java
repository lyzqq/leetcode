package reversePrint;

/**
 * @author 刘勇志
 * @version 1.0
 */
public class ListNode {
    /**
     * @author 刘勇志
     * @version 1.0
     */
     int val;
     ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
